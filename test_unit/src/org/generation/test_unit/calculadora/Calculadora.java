package org.generation.test_unit.calculadora;

public class Calculadora {
	
	public int sumarNumeros (int num1, int num2) {
		return num1 + num2;
	}
	
	public int restarNumeros (int num1, int num2) {
		return num1 - num2;
	}
	
	public int multiplicarNumeros (int num1, int num2) {
		return num1 * num2;
	}
	
	public int dividirNumeros (int num1, int num2) {
		return num1/num2;
	}
	
	

}
